package models;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

@Entity
public class Procedimento implements Serializable {

	// Attributes
	private static final long serialVersionUID = 638403605701366232L;
	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "procedimentos_seq_gen")
	@SequenceGenerator(name = "procedimentos_seq_gen", sequenceName = "procedimentos_id_seq")
	private int id;
	private String nome;
	private double valor;

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Procedimento other = (Procedimento) obj;
		if (id != other.id)
			return false;
		return true;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTipo() {
		return nome;
	}

	public void setTipo(String nome) {
		this.nome = nome;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}

}
